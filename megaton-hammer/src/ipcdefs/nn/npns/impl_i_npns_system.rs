#[derive(Debug)]
pub struct INpnsSystem<T>(T);

impl INpnsSystem<Session> {
	pub fn raw_new() -> Result<INpnsSystem<Session>> {
		use ::ipcdefs::nn::sm::detail::IUserInterface;

		let sm = IUserInterface::raw_new()?;

		let session = sm.get_service(*b"npns:s\0\0")?;
		let object : Self = Session::from(session).into();
		Ok(object)
	}

	pub fn new() -> Result<Arc<INpnsSystem<Session>>> {
		use alloc::arc::Weak;
		use kernel::sync::InternalMutex;
		use core::mem::ManuallyDrop;
		lazy_static! {
			static ref HANDLE : InternalMutex<Weak<INpnsSystem<Session>>> = InternalMutex::new(Weak::new());
		}
		if let Some(hnd) = HANDLE.lock().upgrade() {
			return Ok(hnd)
		}

		if let Some(hnd) = ::loader::get_override_service(*b"npns:s\0\0") {
			let ret = Arc::new(INpnsSystem(ManuallyDrop::into_inner(hnd)));
			::core::mem::forget(ret.clone());
			*HANDLE.lock() = Arc::downgrade(&ret);
			return Ok(ret);
		}

		let hnd = Self::raw_new()?;
		let ret = Arc::new(hnd);
		*HANDLE.lock() = Arc::downgrade(&ret);
		Ok(ret)
	}

	pub fn to_domain(self) -> ::core::result::Result<INpnsSystem<Domain>, (Self, Error)> {
		match self.0.to_domain() {
			Ok(domain) => Ok(INpnsSystem(domain)),
			Err((sess, err)) => Err((INpnsSystem(sess), err))
		}
	}

	pub fn duplicate(&self) -> Result<INpnsSystem<Session>> {
		Ok(INpnsSystem(self.0.duplicate()?))
	}
}

