#[derive(Debug)]
pub struct IHidbusServer<T>(T);

impl IHidbusServer<Session> {
	pub fn raw_new() -> Result<IHidbusServer<Session>> {
		use ::ipcdefs::nn::sm::detail::IUserInterface;

		let sm = IUserInterface::raw_new()?;

		let session = sm.get_service(*b"hidbus\0\0")?;
		let object : Self = Session::from(session).into();
		object.initialize()?;
		Ok(object)
	}

	pub fn new() -> Result<Arc<IHidbusServer<Session>>> {
		use alloc::sync::Weak;
		use kernel::sync::InternalMutex;
		use core::mem::ManuallyDrop;
		lazy_static! {
			static ref HANDLE : InternalMutex<Weak<IHidbusServer<Session>>> = InternalMutex::new(Weak::new());
		}
		if let Some(hnd) = HANDLE.lock().upgrade() {
			return Ok(hnd)
		}

		if let Some(hnd) = ::loader::get_override_service(*b"hidbus\0\0") {
			let ret = Arc::new(IHidbusServer(ManuallyDrop::into_inner(hnd)));
			::core::mem::forget(ret.clone());
			*HANDLE.lock() = Arc::downgrade(&ret);
			return Ok(ret);
		}

		let hnd = Self::raw_new()?;
		let ret = Arc::new(hnd);
		*HANDLE.lock() = Arc::downgrade(&ret);
		Ok(ret)
	}

	pub fn to_domain(self) -> ::core::result::Result<IHidbusServer<Domain>, (Self, Error)> {
		match self.0.to_domain() {
			Ok(domain) => Ok(IHidbusServer(domain)),
			Err((sess, err)) => Err((IHidbusServer(sess), err))
		}
	}

	pub fn duplicate(&self) -> Result<IHidbusServer<Session>> {
		Ok(IHidbusServer(self.0.duplicate()?))
	}
}

