#[derive(Debug)]
pub struct IPowerStateInterface<T>(T);

impl IPowerStateInterface<Session> {
	pub fn raw_new() -> Result<IPowerStateInterface<Session>> {
		use nn::sm::detail::IUserInterface;

		let sm = IUserInterface::new()?;

		let r = sm.get_service(*b"spsm\0\0\0\0").map(|s: KObject| Session::from(s).into());
		if let Ok(service) = r {
			return Ok(service);
		}
		r
	}

	pub fn new() -> Result<Arc<IPowerStateInterface<Session>>> {
		use alloc::arc::Weak;
		use spin::Mutex;
		use core::mem::ManuallyDrop;
		lazy_static! {
			static ref HANDLE : Mutex<Weak<IPowerStateInterface<Session>>> = Mutex::new(Weak::new());
		}
		if let Some(hnd) = HANDLE.lock().upgrade() {
			return Ok(hnd)
		}

		if let Some(hnd) = ::megaton_hammer::loader::get_override_service(*b"spsm\0\0\0\0") {
			let ret = Arc::new(IPowerStateInterface(ManuallyDrop::into_inner(hnd)));
			::core::mem::forget(ret.clone());
			*HANDLE.lock() = Arc::downgrade(&ret);
			return Ok(ret);
		}

		let hnd = Self::raw_new()?;
		let ret = Arc::new(hnd);
		*HANDLE.lock() = Arc::downgrade(&ret);
		Ok(ret)
	}

	pub fn to_domain(self) -> ::core::result::Result<IPowerStateInterface<Domain>, (Self, Error)> {
		match self.0.to_domain() {
			Ok(domain) => Ok(IPowerStateInterface(domain)),
			Err((sess, err)) => Err((IPowerStateInterface(sess), err))
		}
	}

	pub fn duplicate(&self) -> Result<IPowerStateInterface<Session>> {
		Ok(IPowerStateInterface(self.0.duplicate()?))
	}
}

impl<T> Deref for IPowerStateInterface<T> {
	type Target = T;
	fn deref(&self) -> &T {
		&self.0
	}
}
impl<T> DerefMut for IPowerStateInterface<T> {
	fn deref_mut(&mut self) -> &mut T {
		&mut self.0
	}
}
